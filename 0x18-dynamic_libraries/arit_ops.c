#include "arit.h"

/**
 * small functions to perform basic arithmetic operations
 * (+, -, *, /, %) on two integers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result of whichever operation was called
*/

int add(int a, int b)
{
	return (a + b);
}

int sub(int a, int b)
{
	return (a - b);
}

int div(int a, int b)
{
	return (a / b);
}

int mul(int a, int b)
{
	return (a * b);
}

int mod(int a, int b)
{
	return (a % b);
}
